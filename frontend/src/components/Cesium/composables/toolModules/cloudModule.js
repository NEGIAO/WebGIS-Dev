import { getCloudQualityOptions, DEFAULT_CLOUD_QUALITY } from '../../Cloud/cloudQualityPresets';

/**
 * 创建体积云模块
 * @param {import('vue').Ref} cloudParams - 云参数 ref
 * @returns {{ id: string, title: string, description: string, status: string, statusTone: string, controls: Array }}
 */
export function createCloudModule(cloudParams) {
    const params = cloudParams.value;
    const status = params.cloudsEnabled
        ? qualityStatusLabel(params.quality)
        : '未启用';
    const statusTone = params.cloudsEnabled ? 'success' : 'neutral';

    return {
        id: 'cloud',
        title: '体积云·大气',
        description: 'Bruneton 大气 + 体积云 raymarch + BSM 云影/丁达尔（cesium-clouds-atmosphere）',
        status,
        statusTone,
        controls: createCloudControls(params, !params.cloudsEnabled),
    };
}

