/**
 * 模块卡片状态文案：流畅 / 均衡 / 极致
 * @param {string | undefined} quality
 * @returns {string}
 */
function qualityStatusLabel(quality) {
    if (quality === 'balanced') return '均衡';
    if (quality === 'ultra') return '极致';
    return '流畅';
}